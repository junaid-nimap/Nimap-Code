function FindMax(x, y) {
  if (x > y) {
    console.log(x, "is greater");
  } else {
    console.log(y, "is greater");
  }
}
function FindMin(x, y) {
  if (x < y) {
    console.log(x, "is smaller");
  } else {
    console.log(y, "is smaller");
  }
}
let a = 40;
let b = 330;
FindMax(a, b);
FindMin(a, b);
